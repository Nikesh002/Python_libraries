# Different ways to import the Modules
# 1: Directly import the whole Module
# import math
#
# print(math.pi)
# ************************************
# 2: import only specific class/variable/method from the Module
# This is the best practice
# from math import pi
#
# print(pi)
# ************************************
# # 3: Import everything from the Module
# from math import *
#
# print(pi)
# ************************************
